public class StringConcatenation {
    public static void main(String[] args) {
        String str = "Hello";
        int num = 42;
        double pi = 3.14;

        // Конкатенация строки с различными типами
        String result = str + " world! " + num + " and " + pi;

        System.out.println(result); // Hello world! 42 and 3.14
    }
}
